export const getAllQuestions = () => {
  const allQuestions = []
  Object.keys(questionCategories).forEach(category => {
    questionCategories[category].questions.forEach(question => {
      allQuestions.push({
        text: question,
        category: category,
        categoryName: questionCategories[category].name
      })
    })
  })
  return allQuestions
}

// Get all categories including custom ones
// Note: This function should be called from components that can import questionStorage
export const getAllCategories = (customCategories = {}) => {
  const allCategories = { ...questionCategories }
  
  // Add custom categories
  Object.keys(customCategories).forEach(categoryKey => {
    allCategories[categoryKey] = {
      name: customCategories[categoryKey].name,
      icon: customCategories[categoryKey].icon,
      isCustom: true,
      questions: [] // Custom categories get questions from questionStorage
    }
  })
  
  return allCategories
}


